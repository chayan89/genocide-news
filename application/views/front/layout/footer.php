    // Join us
    $('#frmJoinUs').submit(function(e){
        e.preventDefault();
        let title = $('#email').val();
        if(title == ""){
            swalAlert('Email is required', 'warning');
            return false;
        }
        var fd = new FormData(this);
        console.log(fd)
        $.ajax({
            type: "POST",
            url: "<?= base_url()?>" + "Index/joinUsEmailSave",
            data: fd,
            processData: false,
            contentType: false,
            beforeSend: function() {
            },
            success: function(response) {  
                if(response.status.error_code == 0){
                  $('#email').val('');
                  alert(response.status.message);
                  $('#frmJoinUs .btn-default').click();
                  
                }else{
                    alert(response.status.message);
                }
            },
            error: function(response){
                alert('Something wrong, try again');
            } 
        });
    })
